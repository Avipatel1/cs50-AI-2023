import csv
import itertools
import sys

PROBS = {

    # Unconditional probabilities for having gene
    "gene": {
        2: 0.01,
        1: 0.03,
        0: 0.96
    },

    "trait": {

        # Probability of trait given two copies of gene
        2: {
            True: 0.65,
            False: 0.35
        },

        # Probability of trait given one copy of gene
        1: {
            True: 0.56,
            False: 0.44
        },

        # Probability of trait given no gene
        0: {
            True: 0.01,
            False: 0.99
        }
    },

    # Mutation probability
    "mutation": 0.01
}


def main():

    # Check for proper usage
    if len(sys.argv) != 2:
        sys.exit("Usage: python heredity.py data.csv")
    people = load_data(sys.argv[1])

    # Keep track of gene and trait probabilities for each person
    probabilities = {
        person: {
            "gene": {
                2: 0,
                1: 0,
                0: 0
            },
            "trait": {
                True: 0,
                False: 0
            }
        }
        for person in people
    }

    # Loop over all sets of people who might have the trait
    names = set(people)
    for have_trait in powerset(names):

        # Check if current set of people violates known information
        fails_evidence = any(
            (people[person]["trait"] is not None and
             people[person]["trait"] != (person in have_trait))
            for person in names
        )
        if fails_evidence:
            continue

        # Loop over all sets of people who might have the gene
        for one_gene in powerset(names):
            for two_genes in powerset(names - one_gene):

                # Update probabilities with new joint probability
                p = joint_probability(people, one_gene, two_genes, have_trait)
                update(probabilities, one_gene, two_genes, have_trait, p)

    # Ensure probabilities sum to 1
    normalize(probabilities)

    # Print results
    for person in people:
        print(f"{person}:")
        for field in probabilities[person]:
            print(f"  {field.capitalize()}:")
            for value in probabilities[person][field]:
                p = probabilities[person][field][value]
                print(f"    {value}: {p:.4f}")


def load_data(filename):
    """
    Load gene and trait data from a file into a dictionary.
    File assumed to be a CSV containing fields name, mother, father, trait.
    mother, father must both be blank, or both be valid names in the CSV.
    trait should be 0 or 1 if trait is known, blank otherwise.
    """
    data = dict()
    with open(filename) as f:
        reader = csv.DictReader(f)
        for row in reader:
            name = row["name"]
            data[name] = {
                "name": name,
                "mother": row["mother"] or None,
                "father": row["father"] or None,
                "trait": (True if row["trait"] == "1" else
                          False if row["trait"] == "0" else None)
            }
    return data


def powerset(s):
    """
    Return a list of all possible subsets of set s.
    """
    s = list(s)
    return [
        set(s) for s in itertools.chain.from_iterable(
            itertools.combinations(s, r) for r in range(len(s) + 1)
        )
    ]


def joint_probability(people, one_gene, two_genes, have_trait):
    """
    Compute and return a joint probability.

    The probability returned should be the probability that
        * everyone in set `one_gene` has one copy of the gene, and
        * everyone in set `two_genes` has two copies of the gene, and
        * everyone not in `one_gene` or `two_gene` does not have the gene, and
        * everyone in set `have_trait` has the trait, and
        * everyone not in set` have_trait` does not have the trait.
    """
    probability = 1

    for person in people:
        # Determine how many copies of the gene this person has
        if person in two_genes:
            genes = 2
        elif person in one_gene:
            genes = 1
        else:
            genes = 0

        # Whether this person has the trait
        has_trait = person in have_trait

        mother = people[person]["mother"]
        father = people[person]["father"]

        # --- CASE 1: Person has no parental info (use unconditional prob) ---
        if mother is None and father is None:
            gene_prob = PROBS["gene"][genes]

        # --- CASE 2: Person's probability depends on parents ---
        else:
            # Compute each parent's probability of passing the gene
            # (depends on how many copies they have and mutation chance)
            probs_pass = {}
            for parent in [mother, father]:
                if parent in two_genes:
                    # 2 genes → almost always pass gene, except mutation
                    probs_pass[parent] = 1 - PROBS["mutation"]
                elif parent in one_gene:
                    # 1 gene → 50/50 chance of passing gene
                    probs_pass[parent] = 0.5
                else:
                    # 0 genes → can only pass gene if mutation happens
                    probs_pass[parent] = PROBS["mutation"]

            mom_pass = probs_pass[mother]
            dad_pass = probs_pass[father]

            # Now combine parent probabilities depending on child’s gene count
            if genes == 0:
                # Neither parent passes the gene
                gene_prob = (1 - mom_pass) * (1 - dad_pass)
            elif genes == 1:
                # Exactly one parent passes the gene
                gene_prob = mom_pass * (1 - dad_pass) + (1 - mom_pass) * dad_pass
            else:  # genes == 2
                # Both parents pass the gene
                gene_prob = mom_pass * dad_pass

        # --- Probability of having (or not having) the trait given gene count ---
        trait_prob = PROBS["trait"][genes][has_trait]

        # Multiply this person's total probability into the joint
        probability *= gene_prob * trait_prob

    return probability



def update(probabilities, one_gene, two_genes, have_trait, p):
    """
    Add to `probabilities` a new joint probability `p`.
    Each person should have their "gene" and "trait" distributions updated.
    Which value for each distribution is updated depends on whether
    the person is in `have_gene` and `have_trait`, respectively.
    """
    for person in probabilities:
        # Determine how many genes this person has
        if person in two_genes:
            genes = 2
        elif person in one_gene:
            genes = 1
        else:
            genes = 0

        # Whether this person has the trait
        has_trait = person in have_trait

        # Add the joint probability to both gene and trait distributions
        probabilities[person]["gene"][genes] += p
        probabilities[person]["trait"][has_trait] += p
    


def normalize(probabilities):
    """
    Update `probabilities` such that each probability distribution
    is normalized (i.e., sums to 1, with relative proportions the same).
    """
    for person in probabilities:
        # Normalize gene probabilities
        gene_total = sum(probabilities[person]["gene"].values())
        for genes in probabilities[person]["gene"]:
            probabilities[person]["gene"][genes] /= gene_total

        # Normalize trait probabilities
        trait_total = sum(probabilities[person]["trait"].values())
        for trait in probabilities[person]["trait"]:
            probabilities[person]["trait"][trait] /= trait_total


if __name__ == "__main__":
    main()
